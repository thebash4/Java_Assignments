
public class Natural_numbers {

	
	
	public static void main(String[] args) {
		
		//for loop
		int b=0;
		for (int i = 1; i<=10; i++){
			b=b+i;
			System.out.println("b="+ b);
		}
		
		//while loop
		int a =1;
		int sum=0;
		while(a<=10){
		sum=sum+a;
		System.out.println("sum="+ sum);
		a++;
		}
		
		//dowhile loop
		int x=1;
		int total= 0;
	do{
		total=total+x;
		System.out.println("total="+ total);
		x++;
		
	} while (x<=10);
		
		
	}
	
	}


